package com.paulmpanga.inventory.adapters;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import com.paulmpanga.inventory.fragments.LowStockFragment;
import com.paulmpanga.inventory.fragments.OutOfStockFragment;

public class PagerAdapter extends FragmentStatePagerAdapter {
    int mNumOfTabs;

    public PagerAdapter(FragmentManager fm, int NumOfTabs) {
        super(fm);
        this.mNumOfTabs = NumOfTabs;
    }

    @Override
    public Fragment getItem(int position) {

        switch (position) {
            case 0:
                OutOfStockFragment tab1 = new OutOfStockFragment();
                return tab1;
            case 1:
                LowStockFragment tab2 = new LowStockFragment();
                return tab2;
            default:
                return null;
        }
    }

    @Override
    public int getCount() {
        return mNumOfTabs;
    }
}
